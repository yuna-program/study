package operator;

public class Operator1 {
	public static void main(String[] args) {
		
		// 변수 초기화
		
		int a = 5;
		int b = 2;
		
		// 덧셈
		int sum = a + b;
		System.out.println("a + b = " + sum);
		
		// 뺄셈
		int diff = a - b;
		System.out.println("a - b = " + diff);
		
		// 나눗셈
		int div = a / b;
		System.out.println("a / b = " + div);
		
		// 곱셈
		int mul = a*b;
		System.out.println("a * b = " + mul);
		
		// 나머지 연산 
		int remain = a % b;
		System.out.println("a % b = " + remain);
	}

}
